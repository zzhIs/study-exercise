    public ListNode removeNthFromEnd(ListNode head, int n) {
        if (head == null) {
            return null;
        }
        ListNode result = new ListNode(0, head);
        ListNode first = head;
        ListNode second = result;
        for (int i = 0; i < n; i++) {
            first = first.next;
        }
        while (first !=null) {
            first = first.next;
            second = second.next;
        }
        second.next = second.next.next;
        return result.next;
    }
